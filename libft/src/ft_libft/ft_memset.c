#include "libft.h"

void	*ft_memset(void *s, int c, size_t n);

void	*ft_memset(void *s, int c, size_t n)
{
	char	*i;

	i = s;
	while (n)
	{
		*i++ = c;
		n--;
	}
	return (s);
}
